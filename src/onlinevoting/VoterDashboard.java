    public void checkElection(){
        String age = jTextField4.getText();
        String state = jTextField6.getText();
        Date str = new Date();
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        String date = formatter.format(str);
        try {
            String sql = "Select * from Election_table where Election_Id ='1001' ";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if(val.isAge(age)){
                if(rs.next()){
                    String name = rs.getString(2);
                    String edate = rs.getString(3);
                    String estate = rs.getString(4);
                    if(estate.equals(state)){
                        if(edate.equals(date)){
                            //JOptionPane.showMessageDialog(this,"Election is going on, choose your party and submit your vote");
                            jLabel10.setText(name + " Election is going on in your state, choose your party and submit your vote");
                            jButton1.setEnabled(true);
                        }
                        else{
                            //JOptionPane.showMessageDialog(this,"Election is not going no, you can't give your vote to your party");
                            jLabel10.setText("Election is not going no, you can't give your vote to your party");
                            jButton1.setEnabled(false);
                        }
                    }
                    else{
                        //JOptionPane.showMessageDialog(this,"Election is not going no, you can't give your vote to your party");
                        jLabel10.setText("Election is not going no, you can't give your vote to your party");
                        jButton1.setEnabled(false);
                    }
                }
            }
            else {
                //JOptionPane.showMessageDialog(this,"Sorry your are under age, You are not eligible for election.");
                jLabel10.setText("Sorry your are under age, You are not eligible for election");
                jButton1.setEnabled(false);
            }
        }
        catch(Exception e){
            System.out.println(e);
        }
    }
    
