    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String staff_id = jTextField5.getText();
        if(staff_id == ""){
            JOptionPane.showMessageDialog(this,"Please fillup the details.");
        }
        else {
            if(val.isId(staff_id) && val.isNotText(staff_id)){
                try {
                    String sql = "Select * from staff_table where Staff_Id = " + staff_id;
                    Statement st = con.createStatement();
                    ResultSet rs = st.executeQuery(sql);
                    if(rs.next()){
                        jTextField1.setText(rs.getString(2));
                        jTextField2.setText(rs.getString(4));
                        jTextField3.setText(rs.getString(3));
                        jTextField4.setText(rs.getString(5));
                    }
                    else 
                        JOptionPane.showMessageDialog(this,"No data found with this operator id");
                }
                catch(Exception e){
                    System.out.println(e);
                }
            }
            else {
                JOptionPane.showMessageDialog(this,"Staff id should be of 4 digits.");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

