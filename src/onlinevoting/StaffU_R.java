    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String staff_id = jTextField5.getText();
        String name = jTextField1.getText();
        String city = jTextField2.getText();
        String contact = jTextField3.getText();
        String passkey = jTextField4.getText();
        String admin = jLabel10.getText();
        if(val.isId(staff_id)){
            if(name.equals("") && city.equals("") && contact.equals("") && passkey.equals("")){
                JOptionPane.showMessageDialog(this,"Please fill up the details");
            }
            else {
                if(val.isText(name) && val.isText(city)){
                    if(val.isTen(contact) && val.isNotText(contact) && val.isSpecialChar(contact)){
                        try{
                            PreparedStatement ps = con.prepareStatement("UPDATE staff_table set Staff_Name=?,Staff_Contact=?,Staff_City=?,Staff_Passkey=?,Admin_Id=? where Staff_Id=?");
                            //insert into user_table(name, father, city, birth_year,gender,voter_id) values(?,?,?,?,?,?)
                            ps.setString(1, name);
                            ps.setString(2, contact);
                            ps.setString(3, city);
                            ps.setString(4, passkey);
                            ps.setString(5, admin);
                            ps.setString(6, staff_id);
                            ps.executeUpdate();
                            JOptionPane.showMessageDialog(this,"Staff details updated succesfully");
                            jTextField1.setText("");
                            jTextField2.setText("");
                            jTextField3.setText("");
                            jTextField4.setText("");
                            jTextField5.setText("");
                        }
                        catch(Exception f){
                            f.printStackTrace();
                        }
                    }
                    else {
                        JOptionPane.showMessageDialog(this,"Contact no should be 10 digits");
                    }
                }
                else {
                    JOptionPane.showMessageDialog(this,"Staff name and city should not contain digits and special characters");
                }
            }
        }
        else {
            JOptionPane.showMessageDialog(this,"Staff Id should be 4 digits");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

