    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String party = jTextField1.getText();
        String state = jTextField2.getText();
        String leader = jTextField3.getText();
        String candidate = jTextField4.getText();
        String date = jTextField5.getText();
        String fname = jLabel10.getText();
        String admin = jLabel12.getText();
        System.out.println(admin);
        if(party.equals("") || state.equals("") || leader.equals("") || candidate.equals("") || date.equals("")){
            JOptionPane.showMessageDialog(null,"Please enter the details.");
        }
        else {
            if(val.isText(party) && val.isText(state) && val.isText(leader) && val.isText(candidate)){
                if(fname.equals("")) {
                    JOptionPane.showMessageDialog(null,"Please Choose a image file.");
                }
                else{
                    try{
                        PreparedStatement ps = con.prepareStatement("insert into party_table(Party_Name, Party_States, Party_Leader,"
                                + " Party_Candidate, Party_Date, Party_Logo, Admin_Id) values(?,?,?,?,?,?,?)");
                        ps.setString(1, party);
                        ps.setString(3, state);
                        ps.setString(2, leader);
                        ps.setString(4, candidate);
                        ps.setString(5, date);
                        ps.setBytes(6, photo);
                        ps.setString(7, admin);
                        ps.executeUpdate();
                        JOptionPane.showMessageDialog(this,"Party added succesfully");
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField4.setText("");
                        jTextField5.setText("");
                        jLabel10.setText("");
                        jLabel11.setIcon(null);
                    }
                    catch(Exception f){
                        f.printStackTrace();
                    }
                }
            }
            else
                JOptionPane.showMessageDialog(this,"Name, State, Leader and Candidate should not contain digits and special character");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

